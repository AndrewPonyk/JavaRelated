package flowcontrol;

public class CaseStatement {
	public static void main(String[] args) {
		System.out.println("Switch statement");
		
		String s = "hellod";
		
		
		switch (s) {
		case "hellod":
			System.out.println("hellod");
			break;
		case "1":
			System.out.println("1");
			break;
		case "2":
			System.out.println("1");
			break;
		default:
			System.out.println("no value");
			break;
		}
	}
}

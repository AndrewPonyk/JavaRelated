package staff;

public class ModulusOperator {
	public static void main(String[] args) {
		System.out.println(12 % 5); // 2
		
		//If the first operand is negative, so is the result of the modulus is negative
		System.out.println(-12 % 5); //-2
		
		System.out.println(12.4 % 3.2);// 2.8
		System.out.println(5.2 % 2.5); // 0.20000000000000018
	}
}
